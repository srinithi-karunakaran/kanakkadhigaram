"""
ஈ. அரிசி – நெல் மாற்று கணக்கீட்டு நூலகம்
========================================

இந்த module பழமையான தமிழ் கணக்கதிகார அரிசி கணக்குகளை
அடிப்படையாக கொண்டு நெல் → அரிசி மற்றும் அரிசி → நெல்
மாற்றங்களை செயல்படுத்துகிறது.

அம்சங்கள்:
- நெல் → அரிசி விகித மாற்றம்
- அரிசி → நெல் கணக்கீடு
- fractional அடிப்படையிலான மாற்றங்கள்
- பாரம்பரிய அளவியல் கணக்குகள் (கலம், மரக்கால், நாழி)
"""

from fractions import Fraction


# =========================================================
# நெல் → அரிசி (கணக்கு 197)
# =========================================================

def rice_from_paddy_197(paddy_amount: float) -> str:
    """
    பாடல் 197:
    நொல்லோ டரிசி நிறையுணர வேண்டுதிரேல்
    சொல்லாலே சொல்தனைத் தான்மாறி--நல்லதொரு
    மாகாணி யிற்கழிக்க வாணுதலாய் வந்ததுவே
    காகாணி மோசமில்லை காண்

    Parameters
    ----------
    paddy_amount : float
        நெல் அளவு (கலம்)

    Returns
    -------
    str
        அரிசி அளவு தமிழ் விளக்கத்துடன்
    """

    rice = (paddy_amount * 7) / 16

    return (
        f"{paddy_amount} கலம் நெல்லுக்கு சுமார் "
        f"{rice:.2f} கலம் அரிசி கிடைக்கும் "
        f"(2 மரக்கால் நெல் → 7 நாழி அரிசி விகிதப்படி கணக்கிடப்பட்டது)"
    )


# =========================================================
# அரிசி → நெல் (கணக்கு 198)
# =========================================================

def paddy_from_rice_198(rice_amount: float) -> str:
    """
    பாடல் 198:
    அரிசியால் நெல் வேண்டில் ஆய்ந்த பொருள் தன்னை
    வரிசையால் மாறி அரிசிதனை-.............
    ஈரெட்டால் மாறி இசைந்தபொருள் தனையே
    அறுவர்க் கீய்ந்தே உரை

    Parameters
    ----------
    rice_amount : float
        அரிசி அளவு (கலம்)

    Returns
    -------
    str
        நெல் அளவு தமிழ் விளக்கத்துடன்
    """

    paddy = (rice_amount * 16) / 8

    return (
        f"{rice_amount} கலம் அரிசிக்கு சுமார் "
        f"{paddy:.2f} கலம் நெல் தேவை "
        f"(16 நாழி நெல் → 8 நாழி அரிசி மாற்று விகிதப்படி)"
    )


# =========================================================
# நெல் → அரிசி (கணக்கு 199)
# =========================================================

def rice_from_paddy_199(paddy_amount: float) -> str:
    """
    பாடல் 199:
    முல்லை முதலறிந்து நாழித் தொகையாக்கிச்
    சொல்லிய வாயில் கழிப்பனவே--நெல்முதல்
    ஆகும் படியே அணியிழையாய் இக்கணக்கை
    ஓதும் படியே உரை

    Parameters
    ----------
    paddy_amount : float
        நெல் அளவு (கலம்)

    Returns
    -------
    str
        அரிசி அளவு தமிழ் விளக்கத்துடன்
    """

    rice = (paddy_amount * 6) / 16

    return (
        f"{paddy_amount} கலம் நெல்லுக்கு சுமார் "
        f"{rice:.2f} கலம் அரிசி கிடைக்கும் "
        f"(நேரடி விகித மாற்ற கணக்கு)"
    )


# =========================================================
# எளிய விகித கணக்கு (கணக்கு 200)
# =========================================================

def rice_simple_ratio_200(paddy_amount: float) -> str:
    """
    பாடல் 200:
    கூட்டிய விரண்டு மஞ்சுக்கு கூறுநெல் லெட்டு மாவில
    நாட்டியே வைத்து நாலும் நிலமது பொன்ம துவர
    வாட்டநெற் கரிசி தன்னை விளக்கிய வாயாற் றாக்கி
    வோட்டமி லரிசியா மென்று உரைத்தனர் கணக்கர் கூடி.

    Parameters
    ----------
    paddy_amount : float
        நெல் அளவு (கலம்)

    Returns
    -------
    str
        அரிசி அளவு தமிழ் விளக்கத்துடன்
    """

    rice = (paddy_amount * 2) / 5

    return (
        f"{paddy_amount} கலம் நெல்லுக்கு சுமார் "
        f"{rice:.2f} கலம் அரிசி கிடைக்கும் "
        f"(5 நாழி → 2 நாழி விகித அடிப்படையில்)"
    )


