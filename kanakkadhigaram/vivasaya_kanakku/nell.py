from fractions import Fraction


# =========================================================
# grain_volume_conversion
# =========================================================

def grain_volume_conversion(kalam: int) -> dict:
    """
    பாடல் 201
    மரக்கால் நெல்லு கலமாக வகுத்தொரு குழிக்குக்கேட்டால் 
    சேர்த்த கலத்தை மூன்றாக்கி சிறந்த மூன்றை அரைக்காலில்
    பார்த்து மாற்றி நாழிகைக்குப் பகுந்து கொண்ட லக்கத்தைச்
    சேர்த்துக் கொண்ட பேர்தனக்குச் சேராய்த் துரையும் தேன்மொழியே.

    Parameters
    ----------
    kalam : int
        கலம் அளவு

    Returns
    -------
    dict
        நெல் அளவுகள் (தமிழில்)
    """

    marakkal = kalam * 3
    uzhakku = marakkal * 4
    azhakku = uzhakku / 2

    return {
        "கலம்": f"{kalam} கலம்",
        "மரக்கால்": f"{marakkal} மரக்கால்",
        "உழக்கு": f"{uzhakku} உழக்கு",
        "ஆழாக்கு": f"{azhakku} ஆழாக்கு"
    }


# =========================================================
# grain_price_estimation
# =========================================================

def grain_price_estimation(nel: int, base_price: int) -> dict:
    """
    பாடல் 202
    நன்றாகக் கால்பார்க்கில் கலமாக்கித் தன்னுடைய
    மேலான மரக்கால் தான்கூட்டி--முன்னெதிரே
    சொல்லுகின்ற நெல்மரக்கால் தானாக்கி தூமொழியாய்
    வெல்லத்தை...ணமாக மிக்கு.

    Parameters
    ----------
    nel : int
        நெல் அளவு
    base_price : int
        அடிப்படை விலை

    Returns
    -------
    dict
        விலை கணக்கு (தமிழில்)
    """

    total_value = nel * base_price
    adjusted_value = total_value / 20

    return {
        "நெல் அளவு": f"{nel} அளவு",
        "ஒரு அலகு விலை": f"{base_price} பணம்",
        "மொத்த மதிப்பு": f"{total_value} பணம்",
        "சீரமைக்கப்பட்ட மதிப்பு": f"{adjusted_value} பணம்"
    }


# =========================================================
# fractional_grain_conversion
# =========================================================

def fractional_grain_conversion() -> dict:
    """
    பாடல் 203

    மரக்கால் நெல்லை நாழியாக்கி வகுத்தொரு நாழிகேட்டால்
    ...... ...... ......சேர்ந்த நாழிகை யரையாக்கி
    சிறந்த ஆறுமாகாணி தனக்கீந் திவைசெவி டாக்கி
    ...... ...... ......இனிய பேர்க்கு உரைப்பதுவே.

    Parameters
    ----------
    None

    Returns
    -------
    dict
        பகுதி அளவியல் கணக்கு (தமிழில்)
    """

    result = Fraction(1, 2) + Fraction(1, 4) + Fraction(1, 20)

    return {
        "மொத்த பகுதி": f"{result} (fractional அளவு)"
    }


# =========================================================
# trade_profit_analysis
# =========================================================

def trade_profit_analysis(buy_price: float, sell_price: float) -> dict:
    """
    பாடல் 204 
    குயிலைப் போன்ற குரலினையும்
    குஞ்சரம் போன்ற நடையினையும்
    அயிலைப் போன்ற கண்ணினையும்
    அஞ்சுகம் போல மொழியுமுளாய்
    பயிலும் மரக்கால் நாழிநெல்லை
    பகர்மா காணி தனிற்பெருக்கி
    கயனில் வனையை யணிகோதாய்
    கணக்கைச் சீராய்ந் துரைத்திடுமே. 

    Parameters
    ----------
    buy_price : float
        வாங்கிய விலை
    sell_price : float
        விற்ற விலை

    Returns
    -------
    dict
        லாப / நஷ்ட கணக்கு (தமிழில்)
    """

    difference = abs(buy_price - sell_price)
    ratio = buy_price / sell_price if sell_price != 0 else None

    return {
        "வாங்கிய விலை": f"{buy_price} பணம்",
        "விற்ற விலை": f"{sell_price} பணம்",
        "விலை வேறுபாடு": f"{difference} பணம்",
        "விகிதம்": f"{ratio}"
    }


