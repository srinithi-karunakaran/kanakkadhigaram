"""
உலக பிரபஞ்ச அமைப்பு (Ulaga Cosmology System)
===========================================

கணக்கதிகாரம் பாடல் 48 அடிப்படையில்
பிரபஞ்ச அமைப்பு மற்றும் உலக அடுக்குகள் குறித்த விளக்கம்.

This module represents ancient Tamil cosmological structure
based on Kanakkadhigaram verse 48.
"""

# ==========================================================
# DATA SET
# ==========================================================

ULAGA_COSMOLOGY = {
    "பாதாளம்": {
        "description": "பூமிக்குக் கீழுள்ள உலகங்கள்",
        "note": "பாதாளத்தின் சுற்றளவும் அகலமும் குறிப்பிடப்படுகிறது"
    },

    "ஏழு பூமி": {
        "count": 7,
        "description": "ஏழு உலகங்கள் / ஏழு பூமி அமைப்பு"
    },

    "மகாமேரு": {
        "description": "உலகத்தின் மைய மலை",
        "height_unit": "யோசனை",
    }
}


# ==========================================================
# CORE FUNCTIONS
# ==========================================================

def get_cosmology_data(name):
    """
    பாடல் 48

    மகபதி னாறு பாதாளைச் சுற்று அகலமும் அதுவே யாகும்
    ஆதி நாலு நூறாயிரத்தின் மேலே நிகரிலா ஏழு பூமி
    மைய்யா யிரத்தென் மரத்துத் திசையுமாறு மேரு செப்பிய ஏழு
    பூமிகல் பேர்தனை பிராமக பேர்காட்டீரே

    Parameters
    ----------
    name : str
        பொருள் பெயர் (பாதாளம் / ஏழு பூமி / மகாமேரு)

    Returns
    -------
    dict
        பிரபஞ்ச அமைப்பு தகவல்
    """
    if name not in ULAGA_COSMOLOGY:
        raise ValueError(f"தெரியாத பிரபஞ்ச அமைப்பு: {name}")

    return ULAGA_COSMOLOGY[name]


def list_cosmology_entities():
    """
    அனைத்து பிரபஞ்ச அமைப்பு கூறுகளையும் பட்டியலிடும்
    """
    return list(ULAGA_COSMOLOGY.keys())


