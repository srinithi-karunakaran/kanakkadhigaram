def nakshatra_to_nazhi(nakshatra):
    """
    நட்சத்திரம் → நாழிகை கணக்கீடு

    பாடல் 83:
    அசுபதி அறுமீன் குதிரை தலைபோல்
    கலகத்தில் இரண்டு கடிகை செல்லும்
    பரணி மும்மீ னடுப்புப் போல
    அவனியில் நாழிகை ஐந்தே காணே
    கார்த்திகை அறுமீன் தோற்றமை போல
    ஏற்றி சிங்கத் தேக மாகும்
    ரோகணி யும்றால் பன்னிரு மீனா
    மாறொடு சிங்கத்தின் முன்னே படுமே
    மாற்றலை மூன்றும் தேங்காய் கண்ணே
    ஆன்ற சிங்கத் தைந்தே காணே
    ஆதிரை யொருமணி ஆழமை ஆறால்
    ஒத்து புனர்பூச கோலமே கோலமே
    பூசம் புடலாய் பூவது போல
    வீசு கோலுக்கு இரண்டே காணே
    ஆயிலியம் நாலாகும் அம்மூப் போலே
    கோலி நான்கெனக் கூறுவார் கூடே
    மகமொரு நான்கு முடனாகப் போல
    உகளும் தேளுக்கு ஒன்றே காலாம்
    புர முத்திரம் பொற்காற் தொட்டில்
    தேனில் மூன்று உடனிதனி சிற்றறையே
    அத்தன்கோள் மீன்கை தலைதலை துனுசு 

    முத்துந் திண்சிலை மூன்று செல்லும்
    சித்திரை யிருமீன் சிலையஞ் சகமே
    சோதி யொருமணி ...... ......
    விசாக முடிபழந் துரக்காய் சாதி
    சாதி மகர தகருநாற் வுத்தி
    அனுஷம் ஆறு மூடப்பனை போல
    குனியும் கும்பத் தரையா கும்பமே
    கேட்டை நான்கு மீட்டி போல
    வாட்டமில் லாகடம் ஒன்றரை யாகும்
    மூல மைந்தும் எக்காளம் போல
    பூராட முத்திராடம் நீராடும் கட்டில்
    ஒண மூன்று முழக்கோல் போல
    கான லாடு மூன்றெனப் படுமே
    அவிட்டச் சக்கரத் தாரும் ஏழரை
    தவிட்டுச் சதயம் தன்னில் இரண்டேகோல்
    பூரட்டாதி உத்திரட்டாதி பொற்கால் கட்டில்
    ஓரெட்டாத தண்டு ஒன்றரை நான்கே
    ரேவதி ஆறு இளைதரு பீடம்
    நன்றிய கடகத்தில் ஒன்றே காலே

    Parameters
    ----------
    nakshatra : str
        நட்சத்திர பெயர்

    Returns
    -------
    dict
        {
            "nakshatra": name,
            "symbol": symbolic representation,
            "nazhi": night time units
        }
    """

    if nakshatra not in NAKSHATRA_MAP:
        raise ValueError("தெரியாத நட்சத்திரம்")

    return {
        "nakshatra": nakshatra,
        "symbol": NAKSHATRA_MAP[nakshatra]["symbol"],
        "nazhi": NAKSHATRA_MAP[nakshatra]["nazhi"]
    }


# ==========================================================
# 3. TOTAL NIGHT TIME CALCULATION
# ==========================================================

def total_night_nazhi(nakshatra_list):
    """
    பல நட்சத்திரங்களின் இரவு நேர நாழிகை கணக்கீடு

    Parameters
    ----------
    nakshatra_list : list
        நட்சத்திர பட்டியல்

    Returns
    -------
    int
        மொத்த நாழிகை
    """

    total = 0
    for n in nakshatra_list:
        if n in NAKSHATRA_MAP:
            total += NAKSHATRA_MAP[n]["nazhi"]
    return total


# ==========================================================
# 4. SYMBOL LOOKUP
# ==========================================================

def get_symbol(nakshatra):
    """
    நட்சத்திர சின்னம் பெறுதல்
    """

    return NAKSHATRA_MAP.get(nakshatra, {}).get("symbol")


# ==========================================================
# 5. UTILITIES
# ==========================================================

def list_nakshatras():
    """அனைத்து நட்சத்திரங்கள் பட்டியல்"""
    return list(NAKSHATRA_MAP.keys())


