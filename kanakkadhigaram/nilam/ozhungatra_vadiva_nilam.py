# 179 - விசிறி வடிவ நிலம்
# =========================================================

def visiri_vadivu_nilam(a: float, b: float) -> float:
    """
    பாடல் (179)
    காம்பதி யிரண்டு தாக்கும் கனமதி யிரண்டு தாக்கும்
    சோம்பர அளந்து கூட்டிச் சொன்னநா லத்தொன் றாக்கி
    காம்பதில் நெடுங்கொரு தாக்கும் கனமதி யிரண்டு தாக்கும்
    ஆம்படி அளந்து மூன்றொன் றாக்கியே மாற லாமே.

    பொருள்:
    a × b

    Parameters:
        a (float): முதல் அளவு
        b (float): இரண்டாவது அளவு

    Returns:
        float: பரப்பு
    """
    return a * b


# =========================================================
# 180 - வளைவு நிலம்
# =========================================================

def valaivu_vadivu_nilam(inner: float, outer: float) -> float:
    """
    பாடல் (180)
    இறைபோன்ற நிலளக்கு மிடத்துடன் வளைவு
    பிறவளைவு கூட பாகஞ் செய்து
    பிறகையும் பாகஞ் செய்து தனிவலே
    மாறி னதுகை யிற்குழி யாமே

    Parameters:
        inner (float): உள் ஆரம்
        outer (float): வெளி ஆரம்

    Returns:
        float: வளைவு பரப்பு
    """
    return (outer ** 2) - (inner ** 2)


# =========================================================
# 181 - வடிவ நில மாற்றம்
# =========================================================

def vadivu_nilam_mattram(values: list) -> float:
    """
    பாடல் (181)
    அழகாய் வட்டத் தொருவட்ட மாறி
    அழகாய் வருந்துகை ளெல்லாம் - மிழையிடையாய்
    நாலு மாக்கிய வருந்துகை கூட்ட
    கொலியவேற் கண்ணாய் குழி.

    Parameters:
        values (list): அளவுகள் பட்டியல்

    Returns:
        float: மாற்றப்பட்ட மதிப்பு
    """
    return (sum(values) / len(values)) * 10


# =========================================================
# 182 - உட்குறை நிலம் (Obstacle removal)
# =========================================================

def utkura_nilam_kuraippu(total_area: float, obstacles: list) -> float:
    """
    பாடல் (182)
     கருது நிலந்தனில் கட்டைமரம் கிணறு
    நிரவி கலமேடு நின்றால் - உரியதனில்
    கண்டஅடி அந்நிலத்தின் கோல்கணக் காக்கி
    கொண்டதனைத் தாக்கியே கூறு.

    பொருள்:
    total area - obstacles

    Parameters:
        total_area (float): மொத்த நில பரப்பு
        obstacles (list): தடைகளின் பரப்புகள்

    Returns:
        float: நிகர நில பரப்பு
    """
    return total_area - sum(obstacles)

