# =========================================================
# 175 - கலப்பு வடிவ நிலம்
# =========================================================

def kalappu_vadivu_nilam(values: list) -> float:
    """
    பாடல் (175)
    துடிமுழுவு தோரை சுளகெறும்பு சூலம்
    வடிவறிந்து நீளத்தால் மாறி -- நடுவதனை
    வேண்டிய கைகொண்டு விமக்காட்டி னாற்பெருக்கி
    விண்டிய தோராங் குழி.

    பொருள்:
    பல வடிவ நிலங்களின் சராசரி அடிப்படையில் பரப்பு கணக்கிடுதல்

    Parameters:
        values (list): நில அளவுகள் பட்டியல்

    Returns:
        float: கணக்கிடப்பட்ட நில பரப்பு (approx)
    """
    avg = sum(values) / len(values)
    return avg * avg


# =========================================================
# 176 - துடி வடிவ நிலம்
# =========================================================

def thudi_vadivu_nilam(values: list) -> float:
    """
    பாடல் (176)
    துடிமுழுவு தோரை சுளகெறும்பு சூலம்
    துடிமுழுவ மாகிய கோல்தன்னை -- எழின் மூன்றில்
    தாண்டிய கோல்தன்னை தவறாமல் கூறாக்கி
    வேண்டியதைக் கொண்டு விளம்பு.

    பொருள்:
    நடு மதிப்பு × சராசரி

    Parameters:
        values (list): அளவுகள் பட்டியல்

    Returns:
        float: பரப்பு மதிப்பு
    """
    mid = len(values) // 2
    return values[mid] * (sum(values) / len(values))


# =========================================================
# 177 - துடி கலப்பு கணக்கு
# =========================================================

def thudi_kalappu_kalankku(values: list) -> float:
    """
    பாடல் (177)
    துடிக்கும் தளத்துக்கும் மத்தளத் துக்கு
    நெடுங்கை யளந்து குறுங்கையுன் றளந்து
    .... இடத்தி ரலேவலங் கைநெடுங்
    கையினால் தாக்கிக் குழியாம்.

    பொருள்:
    மொத்தம் × சராசரி

    Parameters:
        values (list): அளவுகள் பட்டியல்

    Returns:
        float: கணக்கிடப்பட்ட மதிப்பு
    """
    total = sum(values)
    return total * (total / len(values))


# =========================================================
# 178 - பல வடிவ கலப்பு நிலம்
# =========================================================

def kalappu_vadivu_samayam(values: list) -> float:
    """
    பாடல் (178)
    வில்லாகில் அம்பாகில் விளைவ தாதல்
    மேலான மொழியாதல் விசேட மான
    சொல்லாமவை முக்காதல் சூல மாதல்
    துடியாதல் முழுவாதல் தோரை யாதல்
    மல்லாருந் திண்புயத்தாய் அளந்த கோலை
    மதிதாழ்வி லாமலவர்க் கீந்த பேற்றை
    நில்லாமல் நடுவளந்த கையால் மாறி
    நேராகக் குழியதனை நிகழ்த்து வோமே.

    பொருள்:
    சராசரி²

    Parameters:
        values (list): அளவுகள் பட்டியல்

    Returns:
        float: பரப்பு மதிப்பு
    """
    avg = sum(values) / len(values)
    return avg * avg


