"""
=========================================================
நாற்சதுர நில அளவீடு (பாடல் 107–111)
=========================================================

இது Kanakkadhigaram நூலிலிருந்து எடுக்கப்பட்ட
பழந்தமிழ் நில அளவீட்டு கணித தொகுதி ஆகும்.

இந்த module விளக்கும் கருத்துகள்:

1. நாற்சதுர நிலம் → பரப்பளவு மாற்றம்
2. கோல் அளவு மாற்ற விதி (Scaling Law)
3. inverse square / direct square விதிகள்
4. குழி கணக்கில் விகித மாற்றம்

அடிப்படை விதி:

    புதிய அளவு = பழைய அளவு × (புதிய கோல்² / பழைய கோல்²)

=========================================================
"""

class NilamNarSathuraAlavu:

    # -------------------------------------------------
    #  பாடல் 107 - நில அளவு மாற்றம்
    # -------------------------------------------------
    @staticmethod
    def nilam_alavu_107(old_kol, new_kol, base_kuzhi):
        """
        பாடல் 107
        நன்றாக நாற்சதுரம் ஆனக்கால் நாற்பாற்கும்
        ஒன்றாக வேறொன்று கையையுமே — குன்றாமல்
        முற்கையு மொன்றே மதித்திட்ட கையதனைப்
        பிற்கையால் ஏந்திப் பெருக்கு.

        Parameters:
            old_kol (float)  : பழைய கோல் அளவு
            new_kol (float)  : புதிய கோல் அளவு
            base_kuzhi (float): அடிப்படை குழி அளவு

        விதி:
            base × (new² / old²)

        return:
            float → புதிய குழி அளவு
        """
        return base_kuzhi * (new_kol ** 2) / (old_kol ** 2)

    # -------------------------------------------------
    # பாடல் 108 - விகித நிலம்
    # -------------------------------------------------
    @staticmethod
    def nilam_vigitha_108(a_kol, b_kol, base_kuzhi):
        """
        பாடல் 108
        கைநான்கும் ஒத்து சதகோணம் உண்டாகில்
        கூட்டிப் பிளந்துமாறிச் சொல்லாதே — நடுவிரண்டு
        கோணமும் அளந்து ஒன்றோடொன்றி
        பாதியை மாறிக் குழி.

        விதி:
            (a² / b²) × base
        """
        return base_kuzhi * (a_kol ** 2) / (b_kol ** 2)

    # -------------------------------------------------
    # பாடல் 109 - inverse square land
    # -------------------------------------------------
    @staticmethod
    def nilam_inverse_109(a_kol, b_kol, base_kuzhi):
        """
        பாடல் 109
        கையொடு கையைத் தாக்கி கலந்திரு பிளவு செய்து
        நொய்யதோர் கையை வைத்துப் பெரியதோர் கையால் மாறில்
        பையர வல்குலாளே பாற்கடல் அதனில் தோன்றும்
        வெய்யவன் கதிர்கள் போல் விளங்கிடும் குழிகள் தானே

        பொருள்:
        inverse square law (மாறுபட்ட நில அளவு)
        
        விதி:
            base × (b² / a²)
        """
        return base_kuzhi * (b_kol ** 2) / (a_kol ** 2)

    # -------------------------------------------------
    # பாடல் 110 - நேரியல் மாற்றம்
    # -------------------------------------------------
    @staticmethod
    def nilam_nirai_110(old_kol, new_kol, base_kuzhi):
        """
        பாடல் 110
        பரவுகின்ற கோணத்தைப் பாதிய தாக்கி
        முரணின்றி யோதுங் குழி

        பொருள்:
        நேரியல் அளவு மாற்றம்

        விதி:
            base × (new / old)
        """
        return base_kuzhi * (new_kol / old_kol)

    # -------------------------------------------------
    # பாடல் 111 - square scaling land
    # -------------------------------------------------
    @staticmethod
    def nilam_sathuram_111(old_kol, new_kol, base_kuzhi):
        """
        பாடல் 111

        நாற்சதிர மானநிலத்தை குறுக்கு நெடுக்காய்
        சதிரினா லேயளந் திட்டுவரச் — சீருரைக்
        கூட்டியே மாறக் குழியால் தொகைநகை
        நாடியே நாட்டுங் கணக்கு.

        பொருள்:
        நாற்சதுர நில அளவு scaling

        விதி:
            base × (new² / old²)
        """
        return base_kuzhi * (new_kol ** 2) / (old_kol ** 2)


