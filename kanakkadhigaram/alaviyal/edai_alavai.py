def material_to_weight(material, to_unit="பலம்"):
    """
    பாடல் 31
    சந்தனமற் பூரம்அகில் கனம்பேர் துலாம்குதிரம்
    முந்தவந்த லோக்கியம் 300பலம் - இந்தப்
    படிநாற்பது கழஞ்சுபாகத் துரைப்பர் மாதே
    கடிகையாம்முன் னூற்றுக் கழஞ்சு.

    சந்தனம் 300 பலம் கொண்டது துலாம்; கற்பூரம் 40 பலம்
    கொண்டது குதிரம்; அகில் 300 பலம் கொண்டது லோக்கியம்
    என்றவாறு

    பாடல் 32:
    சாந்து புழுகு சவாதுபன்னீர் கஸ்தூரி
    வாய்ந்த குங்குமமாய் பரணியாம் - சிமிளாய்
    மீந்திருந்து செம்பாம் அண்டம் விடையமாம்
    நூத்துக் கழஞ்சாய்ச் சொல்.

    Parameters
    ----------
    material : str
        பொருள் பெயர் (உதா: சந்தனம்)

    to_unit : str
        இலக்கு அலகு (default = பலம்)

    Returns
    -------
    float
        மாற்றப்பட்ட எடை மதிப்பு
    """

    if material not in MATERIAL_WEIGHT_RULES:
        raise ValueError("தெரியாத பொருள்")

    return MATERIAL_WEIGHT_RULES[material] * EDAI_BASE_VALUES[to_unit]


def list_materials():
    return list(MATERIAL_WEIGHT_RULES.keys())


