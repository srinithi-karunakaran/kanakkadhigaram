"""
எடையறிதல் (Edai Alavai System)
==============================

கணக்கதிகாரம் நூலில் உள்ள எடையறிதல் முறைகளை அடிப்படையாகக் கொண்டு
வீசம் முதல் பாரம் வரை உள்ள பாரம்பரிய எடை அலகுகள் மற்றும்
பொருள் சார்ந்த எடை கணக்கீடுகள் இம்மொட்யூலில் செயல்படுத்தப்பட்டுள்ளன.

இதில் உள்ளவை:
- எடை அலகு மாற்று முறை (வீசம் → பாரம்)
- பொருள் சார்ந்த எடை விதிகள் (சந்தனம், மிளகு போன்றவை)
- சிறப்பு அளவீட்டு விதிகள்

------------------------------------------------------------

This module implements traditional Tamil weight measurement systems
from Kanakkadhigaram.

It includes:
- Hierarchical weight unit conversions (Veesam → Baaram)
- Material-based weight rules (Sandalwood, Pepper, etc.)
- Special measurement rules based on classical Tamil verses
"""

# =========================================================
# 1. WEIGHT HIERARCHY SYSTEM
# =========================================================

EDAI_HIERARCHY = {
    "வீசம்": {"next": "பிளவு", "factor": 2},
    "பிளவு": {"next": "குன்றி", "factor": 2},
    "குன்றி": {"next": "மஞ்சாடி", "factor": 2},
    "மஞ்சாடி": {"next": "கால்கழஞ்சு", "factor": 5},
    "கால்கழஞ்சு": {"next": "கழஞ்சு", "factor": 4},
    "கழஞ்சு": {"next": "கைசு", "factor": 2},
    "கைசு": {"next": "பலம்", "factor": 4},
    "பலம்": {"next": "நிறை", "factor": 100},
    "நிறை": {"next": "துலாம்", "factor": 2},
    "துலாம்": {"next": "பாரம்", "factor": 20}
}


def build_edai_base_values():
    values = {"வீசம்": 1}
    order = [
        "வீசம்", "பிளவு", "குன்றி", "மஞ்சாடி",
        "கால்கழஞ்சு", "கழஞ்சு", "கைசு",
        "பலம்", "நிறை", "துலாம்", "பாரம்"
    ]

    for i in range(len(order) - 1):
        parent = order[i]
        child = EDAI_HIERARCHY[parent]["next"]
        factor = EDAI_HIERARCHY[parent]["factor"]
        values[child] = values[parent] * factor

    return values

EDAI_BASE_VALUES = build_edai_base_values()

