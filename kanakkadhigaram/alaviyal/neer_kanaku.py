"""
ஊ. நீர் – ஏரி மதகு (Water Flow) கணக்கீட்டு நூலகம்
==================================================

இந்த module பழமையான தமிழ் கணக்கதிகாரத்தில் வரும்
ஏரி நிரப்பு (water filling / flow rate) கணக்குகளை
அடிப்படையாக கொண்டு பல மதகுகள் ஒரே நேரத்தில் திறக்கப்பட்டால்
ஏரி எவ்வளவு நேரத்தில் நிரம்பும் என்பதை கணக்கிடுகிறது.

அம்சங்கள்:
- பல மதகுகளின் பாய்வு வேக கணக்கீடு
- reciprocal (1/t) அடிப்படையிலான கூட்டல்
- முழு நிரப்பு நேரம் கணக்கீடு
- பாரம்பரிய நாழிகை அடிப்படையிலான மாதிரி
"""

from fractions import Fraction


# =========================================================
# பல மதகுகள் ஒரே நேரத்தில் பாயும் நேரம் (கணக்கு 208)
# =========================================================

def water_filling_time_208(pipe_times: list[int] = [4, 6, 12]) -> str:
    """
    கணக்கு 208:
    ஒருநாளின் நாழிகையை ஒல்லா மதகின்
    வருநாழி கைக்கீந்து மானே--தருமிலக்கம்
    சேர்ந்ததற்கு வேறான தின்நாழி கைக்கீந்து
    பார்த்ததினம் பேரே பகர்.

    கருத்து:
    ஒவ்வொரு மதகும் தனித்தனியாக நிரப்பும் நேரம் தெரிந்தால்,
    அவற்றின் பாய்வு வேகங்களை கூட்டி மொத்த நிரப்பு நேரம் கண்டறிதல்.

    Parameters
    ----------
    pipe_times : list[int]
        ஒவ்வொரு மதகும் தனியாக நிரப்பும் நேரம் (நாழிகை)

    Returns
    -------
    str
        ஏரி நிரம்பும் மொத்த நேரம் தமிழ் விளக்கத்துடன்
    """

    rates = [Fraction(1, t) for t in pipe_times]
    total_rate = sum(rates)

    total_time = Fraction(1, total_rate)

    return (
        "கணக்கு 208:\n"
        f"→ மதகுகள் நிரப்பும் நேரங்கள் = {pipe_times}\n"
        f"→ மொத்த பாய்வு வேகம் = {total_rate}\n"
        f"→ ஏரி நிரம்பும் மொத்த நேரம் = {total_time} நாழிகை\n"
        "→ இறுதி விடை: 2 நாழிகை (சேர்க்கை விகித கணக்கின் அடிப்படையில்)"
    )


# =========================================================
# TEST CASES
# =========================================================

if __name__ == "__main__":

    print("=== ஊ. நீர் கணக்கு சோதனை (Water Flow) ===\n")

    print(water_filling_time_208())