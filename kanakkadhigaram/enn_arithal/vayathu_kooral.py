"""
வயது கூறுதல் (Animal Lifespan Mapping System)
===========================================

Ancient Tamil Kanakkadhigaram system assigning
lifespan values to animals and beings.

This module contains TWO conflicting traditions
from different manuscripts.

==================================================
VERSE 91 (TRADITION A)
==================================================

ஆனை மனுவாண்டு நூறாக மேதிமா
நாயியொட் டகத்துக்கு ஆண்டோனில்-மானனையாய்
நாலஞ்சு மாறஞ்சே நாலெட்டு நாள்மூன்றே
மூவஞ்சு மெழுபத்து மூன்றே.


==================================================
VERSE 91 (TRADITION B)
==================================================

மனிதர் 100
யானை, குரங்கு, தேவாங்கு 100
ஒட்டகம் 86
குதிரை 32
மாடு 22
மான், புலி, கரடி 20
காக்கை 17
பாம்பு 10
மீன் 100 நாள்
புழு, பூச்சி 40 நாள்

==================================================
"""


# ==========================================================
# VERSION A 
# ==========================================================

LIFESPAN_TABLE_A = {
    "யானை": 100,
    "மனிதன்": 100,
    "மாடு": 20,
    "எருது": 20,
    "எருமை": 30,
    "கிடா": 30,
    "குதிரை": 32,
    "ஆடு": 12,
    "நாய்": 15,
    "ஒட்டகம்": 73
}


# ==========================================================
# VERSION B 
# ==========================================================

LIFESPAN_TABLE_B = {
    "மனிதன்": 100,
    "யானை": 100,
    "குரங்கு": 100,
    "தேவாங்கு": 100,
    "ஒட்டகம்": 86,
    "குதிரை": 32,
    "மாடு": 22,
    "மான்": 20,
    "புலி": 20,
    "கரடி": 20,
    "கழுதை": 20,
    "பன்றி": 20,
    "காக்கை": 17,
    "பாம்பு": 10,
    "மீன்": "100 நாள்",
    "ஈ": "40 நாள்",
    "எறும்பு": "40 நாள்",
    "பூச்சி": "40 நாள்",
    "புழு": "40 நாள்"
}


# ==========================================================
# CORE FUNCTIONS
# ==========================================================

def get_lifespan(animal: str, version: str = "B"):
    """
    Get lifespan of an animal.

    Parameters
    ----------
    animal : str
        Name of animal
    version : str
        "A" or "B"

    Returns
    -------
    int | str | None
    """

    if version == "A":
        return LIFESPAN_TABLE_A.get(animal)

    return LIFESPAN_TABLE_B.get(animal)


# ==========================================================
# COMPARISON TOOL
# ==========================================================

def compare_lifespan(animal: str):
    """
    Compare both traditions
    """

    a = LIFESPAN_TABLE_A.get(animal, "Not found")
    b = LIFESPAN_TABLE_B.get(animal, "Not found")

    print("\n===== வயது ஒப்பீடு =====")
    print(f"விலங்கு: {animal}")
    print(f"Version A: {a}")
    print(f"Version B: {b}")


# ==========================================================
# LIST FUNCTIONS
# ==========================================================

def list_animals(version: str = "B"):
    if version == "A":
        return list(LIFESPAN_TABLE_A.keys())
    return list(LIFESPAN_TABLE_B.keys())


